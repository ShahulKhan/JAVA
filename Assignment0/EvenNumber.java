package Assignment0;

public class EvenNumber {
	
	public static void main(String[] args) {

		// Iterate from 0 to 20 in incremental mode
		for (int i = 0; i <=20; i++) {
			
			// Divide by 2 if the reminder is 0 
			if(i%2==0) {
				
				//then print i
				System.out.println(i);
			}
		}
	}
}
