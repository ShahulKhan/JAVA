package Assignment0;

public class CompareStrings {
	
	public static void main(String[] args) {
		
	// Declare two Strings
	// 1st String as 'name'
	// 2nd String as 'nickname'
	String name = "Shahul";
	String nickname = "Shaw";
		
	// 'name' and 'nickname' is equal boolean print true 
	// If it is not equal it print false
	// .equal is used to compare String name and String nickname
	boolean same = (name).equals(nickname);
	
	// If both 'name' and 'nickname' are same print 'true'
	// Otherwise print 'false'
	System.out.println(same);
	}
}
