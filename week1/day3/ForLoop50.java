package week1.day3;

public class ForLoop50 {
	
public static void main(String[] args) {
	for (int i=0;i<=50; i++) {
		System.out.println(i);
	}
		
	// Reverse for loop
	for (int i=50; i>=0; i--) {
		System.out.println(i);
	}
		
		
	for (int i=10;i<12;i++) {
		System.out.println(i);
	}
		
	// Reverse for loop
	for (int i = 15; i>1; i--) {
		System.out.println(i);
	}
		
}

}
