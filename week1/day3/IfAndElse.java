package week1.day3;

public class IfAndElse {
	
public static void main(String[] args) {
		
	int a = 10 ;
	
	// % for Reminder
	if (a%2==0) {
		System.out.println("10 is an even number");
	}
		
	else {
		System.out.println("10 is an odd number");
	}
	
	
	
	// / for Quotient
	if (a/2==5) {
		System.out.println("10 is an even number");
	} 
	
	else {
		System.out.println("10 is an odd number");

	}
}

}
