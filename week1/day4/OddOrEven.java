package week1.day4;

public class OddOrEven {

public static void main(String[] args) {
		
	// Method 1
	int a=21;
		if (a%2==0) {
			System.out.println("13 is an Even Number");
		}
		else {
			System.out.println("13 is an Odd Number");
		}
		
		
	// Method 2
		if (a%2==1) {
			System.out.println("13 is an Odd Number");
		}
		else {
			System.out.println("13 is an Even Number");
		}
		
		
		
	// Method 3
		if (a/2!=10) {
			System.out.println("13 is an Even number");
		}
		else {
			System.out.println("13 is an Odd number");
		}
		
}

}
